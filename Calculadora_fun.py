def somar(x, y):
    return (x + y)


def subtrair(x, y):
    return (x - y)


def multiplicar(x, y):
    return (x * y)


def dividir(x, y):
    return (x / y)


n1 = float(input("Insira o primeiro numero: "))
n2 = float(input("Insira o segundo numero: "))

somar(n1, n2)
subtrair(n1, n2)
multiplicar(n1, n2)
dividir(n1, n2)
